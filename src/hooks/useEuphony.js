function objectToList(value) {
  return Object.entries(value || {}).map(([id, item]) => ({ id, ...item }))
}

export function useEuphony() {
  const { user } = useAuth()
  const [highlights, setHighlights] = useState([])
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState(null)
  const seeded = useRef(false)

  const highlightsPath = useMemo(() => (
    user ? `users/${user.uid}/pages/euphony/highlights` : null
  ), [user])

  useEffect(() => {
    if (!highlightsPath) {
      setHighlights([])
      setLoading(false)
      return undefined
    }

    setLoading(true)
    setError(null)

    return onValue(
      ref(db, highlightsPath),
      async (snapshot) => {
        const items = objectToList(snapshot.val()).sort((a, b) => (b.createdAt || 0) - (a.createdAt || 0))
        setHighlights(items)
        setLoading(false)

        if (items.length === 0 && !seeded.current) {
          seeded.current = true
          const updates = {}
          DEFAULT_HIGHLIGHTS.forEach((highlight, index) => {
            updates[`${highlightsPath}/${highlight.id}`] = {
              text: highlight.text,
              highlightedAt: highlight.highlightedAt,
              bookTitle: highlight.bookTitle,
              author: highlight.author || '',
              order: index + 1,
              createdAt: Date.now() - index,
              updatedAt: Date.now() - index,
              createdAtServer: serverTimestamp(),
              updatedAtServer: serverTimestamp(),
            }
          })
          await update(ref(db), updates)
        } else {
          const existingIds = new Set(items.map(item => item.id))
          const backfill = {}
          DEFAULT_HIGHLIGHTS.forEach((highlight, index) => {
            if (!existingIds.has(highlight.id)) {
              backfill[`${highlightsPath}/${highlight.id}`] = {
                text: highlight.text,
                highlightedAt: highlight.highlightedAt,
                bookTitle: highlight.bookTitle,
                author: highlight.author || '',
                order: index + 1,
                createdAt: Date.now() - index,
                updatedAt: Date.now() - index,
                createdAtServer: serverTimestamp(),
                updatedAtServer: serverTimestamp(),
              }
            } else {
              const existing = items.find(item => item.id === highlight.id)
              if (existing && existing.author !== (highlight.author || '')) {
                backfill[`${highlightsPath}/${highlight.id}/author`] = highlight.author || ''
              }
            }
          })
          if (Object.keys(backfill).length > 0) {
            await update(ref(db), backfill)
          }
        }
      },
      (e) => {
        console.error(e)
        setError(e.message)
        setLoading(false)
      }
    )
  }, [highlightsPath])

  const updateHighlight = useCallback(async (highlightId, payload) => {
    if (!highlightsPath || !highlightId) return
    await update(ref(db, `${highlightsPath}/${highlightId}`), {
      ...payload,
      updatedAt: Date.now(),
      updatedAtServer: serverTimestamp(),
    })
  }, [highlightsPath])

  const removeHighlight = useCallback(async (highlightId) => {
    if (!highlightsPath || !highlightId) return
    await remove(ref(db, `${highlightsPath}/${highlightId}`))
  }, [highlightsPath])

  const addHighlight = useCallback(async () => {
    if (!highlightsPath) return null

    const highlightRef = push(ref(db, highlightsPath))
    const now = Date.now()
    const payload = {
      text: '새 하이라이트 문장을 입력하세요.',
      highlightedAt: new Date(now).toLocaleString('ko-KR', {
        year: 'numeric',
        month: '2-digit',
        day: '2-digit',
        hour: '2-digit',
        minute: '2-digit',
      }),
      bookTitle: '책 제목',
      author: '저자',
      order: 0,
      createdAt: now,
      updatedAt: now,
      createdAtServer: serverTimestamp(),
      updatedAtServer: serverTimestamp(),
    }

    await set(highlightRef, payload)
    return { id: highlightRef.key, ...payload }
  }, [highlightsPath])

  return {
    highlights,
    loading,
    error,
    addHighlight,
    updateHighlight,
    removeHighlight,
  }
}
